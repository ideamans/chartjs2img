            v-if="content.aiReady.intro"
            class="opacity-70 mt-3 max-w-2xl mx-auto"
          >{{ content.aiReady.intro }}</p>
        </header>
        <ul class="c2i-ai__grid grid gap-4 md:gap-5 sm:grid-cols-2 lg:grid-cols-4">
          <li v-for="(item, i) in content.aiReady.items" :key="i" class="c2i-ai__card">
            <a :href="item.link" class="c2i-ai__link">
              <h3 class="c2i-ai__title">{{ item.title }}</h3>
              <p class="c2i-ai__body">{{ item.body }}</p>
