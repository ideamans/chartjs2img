<script setup lang="ts">
/**
 * Landing page for /en/ and /ja/ — modeled on gridgram's Landing.vue
 * but stripped to the sections that apply to chartjs2img:
 *
 *   1. Hero            — title + tagline + CTAs
 *   2. Carousel        — gallery PNG marquee
 *   3. Features        — four cards, one sentence each
 *   4. AI-ready        — four agent-integration channels
 *   5. Final CTA       — repeat of hero CTAs at page end
 *   6. Acknowledgments — upstream open-source projects
 *
 * All user-facing strings come from the page's `landing:` frontmatter
 * so the template stays locale-agnostic.
 */
import { computed } from 'vue'
import { useData } from 'vitepress'

interface CtaLink {
  text: string
  link: string
}
interface FeatureItem {
  title: string
  body: string
}
interface AiReadyItem {
  title: string
  body: string
  link: string
  linkText?: string
}
interface AiReadyContent {
  eyebrow?: string
  title: string
  intro?: string
  items: AiReadyItem[]
}
interface AcknowledgmentsContent {
  title: string
  intro?: string
}
interface LandingFrontmatter {
  hero: {
    name: string
    text: string
    primary?: CtaLink
    ai?: CtaLink
    secondary?: CtaLink
  }
  features?: { items: FeatureItem[] }
  aiReady?: AiReadyContent
  finalCta?: {
    title: string
    text?: string
    primary?: CtaLink
    ai?: CtaLink
    secondary?: CtaLink
  }
  acknowledgments?: AcknowledgmentsContent
}

const { frontmatter } = useData()
const content = computed<LandingFrontmatter>(() => (frontmatter.value as any).landing ?? {})

/** Gallery PNGs (generated by `bun run docs:examples`) used for the
 *  hero marquee. A representative subset of the 18 built-in examples. */
const carouselExamples = [
  '01-bar-chart',
  '03-line-chart',
  '05-pie-chart',
  '07-radar-chart',
  '10-bubble-chart',
  '11-mixed-chart-bar-line',
  '13-bar-with-data-labels',
  '14-line-with-annotation',
  '15-treemap-chart',
  '18-japanese-labels',
]
/** Duplicated so the marquee can translate by -50% for a seamless loop. */
const carouselTrack = [...carouselExamples, ...carouselExamples]

// ----- Acknowledgments (objective data; titles come from frontmatter) ---
interface Acknowledgment {
  name: string
  href: string
  license: string
  role: string
  role_ja: string
}
const acknowledgments: Acknowledgment[] = [
