      </div>
    </section>

    <!-- ========================== Acknowledgments ========================== -->
    <section v-if="content.acknowledgments" class="py-16 md:py-20 px-6 bg-base-200">
      <div class="max-w-5xl mx-auto">
        <header class="text-center mb-10">
          <h2 class="text-3xl md:text-4xl font-bold tracking-tight">
            {{ content.acknowledgments.title }}
          </h2>
          <p
            v-if="content.acknowledgments.intro"
            class="opacity-70 mt-3 max-w-2xl mx-auto"
          >{{ content.acknowledgments.intro }}</p>
        </header>
        <ul class="c2i-ack grid sm:grid-cols-2 lg:grid-cols-3 gap-4 md:gap-5">
          <li v-for="(a, i) in acknowledgments" :key="i" class="c2i-ack__card">
            <a :href="a.href" target="_blank" rel="noopener" class="c2i-ack__name">
              {{ a.name }} ↗
            </a>
            <span class="c2i-ack__license">{{ a.license }}</span>
            <p class="c2i-ack__role">
